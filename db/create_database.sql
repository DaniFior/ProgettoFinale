-- Creazione del database con le tabelle impiegato e datore

CREATE TABLE IF NOT EXISTS impiegato (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    nome TEXT NOT NULL,
    cognome TEXT NOT NULL,
    data_nascita DATE NOT NULL,
    ruolo TEXT NOT NULL,
    stipendio REAL NOT NULL,
    id_datore INTEGER,
    FOREIGN KEY (id_datore) REFERENCES datore(id)
);

CREATE TABLE IF NOT EXISTS datore (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    nome TEXT NOT NULL,
    cognome TEXT NOT NULL,
    azienda TEXT NOT NULL
);

